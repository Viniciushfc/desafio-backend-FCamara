package br.com.gerenciamento.entities.vehicle;

/**
 * Este enum representa os tipos de veículos.
 * <p>
 * O enum define os tipos de veículos suportados pelo sistema, incluindo BIKE (moto) e CAR (carro).
 * <p>
 * <p>
 * Os tipos de veículos são utilizados para categorizar os veículos em diferentes classes.
 * <p>
 * <p>
 * Este enum é utilizado como atributo na classe Vehicle para representar o tipo de veículo de cada instância.
 *
 * @author ViniciusHFC
 */
public enum TypeVehicle {
    /**
     * Representa uma moto.
     */
    BIKE,
    /**
     * Representa um carro.
     */
    CAR
}
